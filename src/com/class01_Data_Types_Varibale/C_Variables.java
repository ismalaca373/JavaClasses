package com.class01_Data_Types_Varibale;

public class C_Variables {

	public static void main (String [] args) {
		
//	to create a variable we need :
		
//		data type / name(identifier) = value
//		
			int         a 	        =  19 ;
		
		// or
		
		int b;
		
		b = 12;
		
		System.out.print(b); // 12
		
		
		
		byte b=20;    // ncok kullanilan int,double, float
		
			System.out.println(b); //20
		
		short  s=3733; // cok nadir kullanilir int cok kulaniliyor 
		
			System.out.println(s); // 3730
		
		int	i=100001; 
		
			System.out.println(i); //100001
		
		long l=320000000000000020l;  //l koyuyorsun sonuna
		
			System.out.println(l); // 320000000000000020
		
		float f=10.37f;   // f koyuyorsun sonuna
		
			System.out.println(f);
		
		double d=23923.324932923;   // virgullu olanlarda bu kulnailiyor genelde paralar 
		
			System.out.println(d); // 23923.324932923
		
		boolean white=true;			// sadece iki outcome var. true or false
		
			System.out.println(white); //true
		
		boolean black=false;
		
			System.out.println(black);	//false
		
		char ch= '%';   // single charecter cikartabiliyorsun sadece, ' ' arasinda yazilir
		
			System.out.println(ch); // %
		
		
		
		
				
				
				
		
	}
	
}
